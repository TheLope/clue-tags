function fixSpells(widget) {
	const oldId = widget.componentId;
	const widgetName = widgetsBefore[oldId];
	if (widgetName) {
		const newId = widgetsAfter[widgetName];
		if (newId) {
			return {
				...widget,
				componentId: newId,
			};
		} else {
			return null;
		}
	}
	return widget;
}

function removeCharterMap(widget) {
	const componentId = widget.componentId;
	const widgetName = charterMapBefore[componentId];
	if (widgetName) {
		return null;
	}
	return widget;
}

var convert = document.getElementById("convert-pre-sailing");
var copy = document.getElementById("copy");

convert.addEventListener('click', () => {
    var tagsBefore = document.getElementById("tags-before");
    var tagsAfter = document.getElementById("tags-after");
    var fixSpellsSelected = document.getElementById("fix-spells").checked;
    var removeCharterMapSelected = document.getElementById("remove-charter-map").checked;

    var tags = JSON.parse(tagsBefore.value);

    var arr = tags.map(tag => {
			const widgets = tag.widgetIds ? tag.widgetIds.map(widget => {
				if (fixSpellsSelected) {
					widget = fixSpells(widget);
				}
				if (removeCharterMapSelected) {
					widget = removeCharterMap(widget);
				}
				return widget;
			}).filter(widget => widget) : null;

			if (!tag.widgetIds && !widgets) {
				return tag;
			}

			return {
				...tag,
				widgetIds: widgets,
			}
    });

    var converted = JSON.stringify(arr);

    tagsAfter.style.display = "block";
    tagsAfter.value = converted;

    var tooltip = document.getElementById("convertTooltip");
    tooltip.innerHTML = "Fixed";
})

copy.addEventListener('click', () => {
    var copyText = document.getElementById("tags-after");

    copyText.select();
    copyText.setSelectionRange(0, 99999);
    navigator.clipboard.writeText(copyText.value);

    var tooltip = document.getElementById("copyTooltip");
    tooltip.innerHTML = "Copied";
})
